import { hammingWeight } from "./191_number-of-1-bits";

test.each([
  [0b00000000000000000000000000001011, 3],
  [0b00000000000000000000000000001000, 1],
  [0b11111111111111111111111111111101, 31],
  [0b00000000000000000000000000000000, 0],
  [0b11111111111111111111111111111111, 32],
])("test", (input, output) => {
  expect(hammingWeight(input)).toStrictEqual(output);
});
